export type RoomPhotoOptionGroup = typeof roomPhotoOptionGroups[number];
export type RoomPhotoOptionSection = keyof RoomPhotoOptionGroup['sections'];

type RoomPhotoOptionImage = {
  readonly id: string;
  readonly url: string;
  readonly source: string;
};

type RoomPhotoOptionSectionData = {
  readonly key: string;
  readonly title: string;
  readonly sourceLabel: string;
  readonly amenities: readonly string[];
  readonly images: readonly RoomPhotoOptionImage[];
  readonly notes: readonly string[];
};

export const getRoomPhotoOptionGroup = (roomId: string) => (
  roomPhotoOptionGroups.find((group) => group.roomId === roomId)
);

export const getRoomPhotoOptions = (roomId: string) => {
  const group = getRoomPhotoOptionGroup(roomId);
  if (!group) return [];

  const sections = Object.values(group.sections) as readonly RoomPhotoOptionSectionData[];

  return sections.flatMap((section) => (
    section.images.map((image: RoomPhotoOptionImage, index: number) => ({
      ...image,
      sectionKey: section.key,
      sectionTitle: section.title,
      sectionIndex: index + 1,
    }))
  ));
};
